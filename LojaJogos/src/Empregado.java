
public class Empregado {
	String nome;
	
	//----CONSTRUTOR
	public Empregado(String Nome) {
		this.nome = Nome;
	}
	
	
	//----GETTERS
    public String getNome() {
        return nome;
    }
    
    
    //----SETTERS
    public void setNome(String Nome) {
        this.nome = Nome;
    }
    
    

}
